#!/usr/bin/env python
"""Bake data/items.json -> LivingWeapon/meta.json (id -> {name, wp, cat, formula, flavor}).

The Living Weapon C# runtime reads this to route growth (PA/MA/Speed by category +
formula), to label the card, and to anchor the in-card Kills counter to the right
weapon by its FLAVOR line. Regenerated by BuildLinked.ps1 / Publish.ps1 before each
build so the mod ships self-contained.

The flavor line is the STABLE part of a weapon's description (it doesn't change as the
blade levels up), and it leads the description block that ends in the "Kills: 0" line,
so the runtime can find each weapon's own counter by the nearest flavor before a
"Kills: " -- reliable per-weapon, unlike anchoring to the name (which lives in a
separate UI buffer far from the description copy).
"""
import json
from pathlib import Path
from patch_names import flavor   # same deterministic flavor() the descriptions are baked from

ROOT = Path(__file__).resolve().parent.parent
ITEMS = ROOT / "data" / "items.json"
OUT = ROOT / "LivingWeapon" / "meta.json"

WEAPON_CATS = {"Knife", "NinjaBlade", "Sword", "KnightSword", "Katana", "Axe", "Rod", "Staff",
               "Flail", "Gun", "Crossbow", "Bow", "Instrument", "Book", "Polearm", "Pole", "Bag", "Cloth"}


def flavor_anchor(it):
    """The exact flavor line that leads this weapon's rendered description -- mirrors
    patch_names: a custom `desc` uses its first line; otherwise flavorOverride or flavor()."""
    custom = it.get("desc")
    if custom:
        return custom.split("\n", 1)[0]
    return it.get("flavorOverride") or flavor(it)


def main():
    doc = json.loads(ITEMS.read_text(encoding="utf-8"))
    meta = {}
    for it in doc["items"]:
        p = it.get("proposed", {}) or {}
        cat = p.get("categoryOverride") or it.get("category")
        if cat not in WEAPON_CATS:
            continue
        if it.get("noGrowth"):
            continue   # excluded from the Living Weapon + system (e.g. Materia Blade)
        name = it.get("name")
        if not name or name == "TBD":
            continue
        wp = p.get("wp") or (it.get("baseline", {}) or {}).get("wp") or 0
        formula = p.get("formula") if isinstance(p.get("formula"), int) else 1
        entry = {
            "name": name, "wp": int(wp), "cat": cat, "formula": int(formula),
            "flavor": flavor_anchor(it),
        }
        sig = it.get("signature")
        if sig:   # iconic tier-grant; emit only the fields the runtime uses (drop curator notes).
            # abilityId/slot are absent on a timed stat grant (e.g. Galewind's Speed +3) -> default them.
            entry["signature"] = {
                "abilityId": int(sig.get("abilityId", 0)), "slot": sig.get("slot", ""), "atTier": int(sig["atTier"]),
                "displayLabel": sig.get("displayLabel", ""),
            }
            if sig.get("hpBelow"):   # conditional HP-gate (e.g. Mortal Coil: Attack Boost while HP < 50%)
                entry["signature"]["hpBelow"] = int(sig["hpBelow"])
            if sig.get("forTurns"):  # timed stat grant (e.g. Galewind: Speed +3 for the first 3 turns)
                entry["signature"]["stat"] = sig.get("stat", "")
                entry["signature"]["statBonus"] = int(sig.get("statBonus", 0))
                entry["signature"]["forTurns"] = int(sig["forTurns"])
            if sig.get("charmLockTurns"):  # charm-lock aura (Galewind): landed Charm unbreakable N turns
                entry["signature"]["charmLockTurns"] = int(sig["charmLockTurns"])
            if sig.get("doomCountdownTo"):  # doom-hasten aura (Eclipsebolt "Eagle Eye"): force enemy Doom to this
                entry["signature"]["doomCountdownTo"] = int(sig["doomCountdownTo"])
            if sig.get("ricochetRadius"):  # ricochet aura (Stormarc "Arc Lightning"): chip to nearest foe
                entry["signature"]["ricochetRadius"] = int(sig["ricochetRadius"])
                entry["signature"]["ricochetPct"] = int(sig.get("ricochetPct", 50))
            if sig.get("crippleTurns"):  # maim (Huntress "Maim"): struck enemies lose reactions N turns
                entry["signature"]["crippleTurns"] = int(sig["crippleTurns"])
            if sig.get("grantCommandAbilityId"):  # barrage (Yoichi "Barrage"): grant JobCommand ability
                entry["signature"]["grantCommandAbilityId"] = int(sig["grantCommandAbilityId"])
            if sig.get("lifeSapOnKill"):  # life sap (Umbral "Life Sap"): a kill heals the wielder
                entry["signature"]["lifeSapOnKill"] = True
            if sig.get("regenSplashRadius"):  # wyrmblood (Dragon Rod): turn-edge regen splash to allies
                entry["signature"]["regenSplashRadius"] = int(sig["regenSplashRadius"])
            if sig.get("moveAbilityIds"):  # spiritual font (Wellspring): movement bits OR-set + held
                entry["signature"]["moveAbilityIds"] = [int(x) for x in sig["moveAbilityIds"]]
        meta[str(it["id"])] = entry
    OUT.write_text(json.dumps(meta, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"wrote {OUT} ({len(meta)} weapons)")


if __name__ == "__main__":
    main()
