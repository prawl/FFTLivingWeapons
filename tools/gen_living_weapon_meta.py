#!/usr/bin/env python
"""Bake data/items.json -> LivingWeapon/meta.json (id -> {name, wp, cat, formula}).

The Living Weapon C# runtime reads this to route growth (PA/MA/Speed by category +
formula) and to label the card. Regenerated by BuildLinked.ps1 / Publish.ps1 before
each build so the mod ships self-contained.
"""
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
ITEMS = ROOT / "data" / "items.json"
OUT = ROOT / "LivingWeapon" / "meta.json"

WEAPON_CATS = {"Knife", "NinjaBlade", "Sword", "KnightSword", "Katana", "Axe", "Rod", "Staff",
               "Flail", "Gun", "Crossbow", "Bow", "Instrument", "Book", "Polearm", "Pole", "Bag", "Cloth"}


def main():
    doc = json.loads(ITEMS.read_text(encoding="utf-8"))
    meta = {}
    for it in doc["items"]:
        p = it.get("proposed", {}) or {}
        cat = p.get("categoryOverride") or it.get("category")
        if cat not in WEAPON_CATS:
            continue
        name = it.get("name")
        if not name or name == "TBD":
            continue
        wp = p.get("wp") or (it.get("baseline", {}) or {}).get("wp") or 0
        formula = p.get("formula") if isinstance(p.get("formula"), int) else 1
        meta[str(it["id"])] = {"name": name, "wp": int(wp), "cat": cat, "formula": int(formula)}
    OUT.write_text(json.dumps(meta, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"wrote {OUT} ({len(meta)} weapons)")


if __name__ == "__main__":
    main()
